function generateTeamMemberCards(teamArray, containerId) {
    const teamContainer = document.getElementById(containerId);

    teamArray.forEach((member) => {
        const memberCard = document.createElement("div");
        memberCard.className = "member-card";

        const memberLink = document.createElement("a");
        memberLink.href = member.github;
        memberLink.target = "_blank";

        const memberDetails = document.createElement("div");
        memberDetails.className = "member-details";

        const memberImage = document.createElement("img");
        memberImage.className = "member-image";
        memberImage.src = member.image;
        memberImage.alt = "";

        const memberData = document.createElement("div");
        memberData.className = "member-data";

        const memberName = document.createElement("p");
        memberName.className = "member-name";
        memberName.textContent = member.name;

        const memberRole = document.createElement("p");
        memberRole.className = "member-role";
        memberRole.textContent = member.role;

        memberData.appendChild(memberName);
        memberData.appendChild(memberRole);

        memberDetails.appendChild(memberImage);
        memberDetails.appendChild(memberData);

        memberLink.appendChild(memberDetails);
        memberCard.appendChild(memberLink);

        teamContainer.appendChild(memberCard);
    });
}






